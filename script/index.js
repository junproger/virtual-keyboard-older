class KeyBoard {
  constructor(lang = 'key_eng') {
    this.key_caps = false;
    this.key_shift = false;
    this.key_lang = lang;
    if (localStorage.getItem('jun#2774VkbLang')) {
      this.key_lang = localStorage.getItem('jun#2774VkbLang');
    } else {
      this.key_lang = 'key_eng';
    }
  }

  createKeyBoardFrame() {
    const elementBody = document.body;
    elementBody.classList.add('body');
    elementBody.insertAdjacentHTML('afterbegin', '<div class="wrap"></div>');
    const elementWrap = document.querySelector('.wrap');
    elementWrap.insertAdjacentHTML('beforeend', '<textarea class="textarea" id="AREA" autofocus></textarea>');
    this.nodeAREA = document.getElementById('AREA');
    elementWrap.insertAdjacentHTML('beforeend', '<p class="inform">Switch language: Left Alt + Left Shift. Designed for Windows</p>');
    elementWrap.insertAdjacentHTML('beforeend', '<div class="keyboard" id="KEYB"></div>');
    this.elementVKB = document.querySelector('.keyboard');
    this.createKeysFragments();
  }

  createKeysFragments() {
    const keyFragment = document.createDocumentFragment();
    for (let i = 0; i <= 63; i += 1) {
      const item = document.createElement('div');
      item.setAttribute('class', 'vkbkeys');
      if (i === 13 || i === 29 || i === 41 || i === 42 || i === 54) {
        item.classList.add('keysize_3');
      } else if (i === 0 || i === 14 || i === 28 || i === 53) {
        item.classList.add('keysize_1x');
      } else if (i === 55 || i === 59) {
        item.classList.add('keysize_2');
      } else if (i === 58) {
        item.classList.add('keysize_4');
      } else {
        item.classList.add('keysize_1');
      }
      if (i > 54 && i < 58) {
        item.classList.add('keyscolor');
      } else if (i > 58 && i < 64) {
        item.classList.add('keyscolor');
      }
      item.setAttribute('id', `${keycode[i]}`);
      keyFragment.append(item);
    }
    this.elementVKB.append(keyFragment);
    this.createKeysLayout();
  }

  createKeysLayout() {
    const childrenVKB = document.getElementById('KEYB').children;
    function parseKeys(data, part) {
      if (data === 'key_eng') {
        for (let j = 0; j <= 63; j += 1) {
          childrenVKB[j].textContent = EngKeysData[`${part}`][`${keycode[j]}`];
        }
      } else if (data === 'key_rus') {
        for (let j = 0; j <= 63; j += 1) {
          childrenVKB[j].textContent = RusKeysData[`${part}`][`${keycode[j]}`];
        }
      }
    }
    if (this.key_lang === 'key_eng' && (this.key_caps === false && this.key_shift === false)) {
      parseKeys('key_eng', 'key_eng');
    } else if (this.key_lang === 'key_eng' && (this.key_caps === true && this.key_shift === true)) {
      parseKeys('key_eng', 'eng_caps_shift');
    } else if (this.key_lang === 'key_rus' && (this.key_caps === false && this.key_shift === false)) {
      parseKeys('key_rus', 'key_rus');
    } else if (this.key_lang === 'key_rus' && (this.key_caps === true && this.key_shift === true)) {
      parseKeys('key_rus', 'rus_caps_shift');
    } else if (this.key_lang === 'key_eng' && this.key_caps === true) {
      parseKeys('key_eng', 'eng_caps');
    } else if (this.key_lang === 'key_rus' && this.key_caps === true) {
      parseKeys('key_rus', 'rus_caps');
    } else if (this.key_lang === 'key_eng' && this.key_shift === true) {
      parseKeys('key_eng', 'eng_shift');
    } else if (this.key_lang === 'key_rus' && this.key_shift === true) {
      parseKeys('key_rus', 'rus_shift');
    }
  }

  printCurrentKey(code) {
    if (this.key_lang === 'key_eng' && (this.key_caps === false && this.key_shift === false)) {
      this.nodeAREA.value += EngKeysData.key_eng[`${code}`];
    } else if (this.key_lang === 'key_eng' && (this.key_caps === true && this.key_shift === true)) {
      this.nodeAREA.value += EngKeysData.eng_caps_shift[`${code}`];
    } else if (this.key_lang === 'key_rus' && (this.key_caps === false && this.key_shift === false)) {
      this.nodeAREA.value += RusKeysData.key_rus[`${code}`];
    } else if (this.key_lang === 'key_rus' && (this.key_caps === true && this.key_shift === true)) {
      this.nodeAREA.value += RusKeysData.rus_caps_shift[`${code}`];
    } else if (this.key_lang === 'key_eng' && this.key_caps === true) {
      this.nodeAREA.value += EngKeysData.eng_caps[`${code}`];
    } else if (this.key_lang === 'key_rus' && this.key_caps === true) {
      this.nodeAREA.value += RusKeysData.rus_caps[`${code}`];
    } else if (this.key_lang === 'key_eng' && this.key_shift === true) {
      this.nodeAREA.value += EngKeysData.eng_shift[`${code}`];
    } else if (this.key_lang === 'key_rus' && this.key_shift === true) {
      this.nodeAREA.value += RusKeysData.rus_shift[`${code}`];
    }
  }

  editSelectedKeys(exec) {
    const startIndex = this.nodeAREA.selectionStart;
    const finishIndex = this.nodeAREA.selectionEnd;
    if ((exec === 'backsp') && (finishIndex !== 0)) {
      this.nodeAREA.value = this.nodeAREA.value.replace((this.nodeAREA.value.charAt(finishIndex - 1)), '');
      this.nodeAREA.selectionStart = finishIndex - 1;
      this.nodeAREA.selectionEnd = finishIndex - 1;
      this.nodeAREA.focus();
    } else if ((exec === 'backsp') && (finishIndex === 0)) {
      this.nodeAREA.selectionStart = 0;
      this.nodeAREA.selectionEnd = 0;
      this.nodeAREA.focus();
    }
    if ((exec === 'delete') && (startIndex !== this.nodeAREA.value.length)) {
      this.nodeAREA.value = this.nodeAREA.value.replace((this.nodeAREA.value.charAt(startIndex)), '');
      this.nodeAREA.selectionStart = startIndex;
      this.nodeAREA.selectionEnd = startIndex;
      this.nodeAREA.focus();
    } else if ((exec === 'delete') && (startIndex === this.nodeAREA.value.length)) {
      this.nodeAREA.selectionStart = this.nodeAREA.value.length;
      this.nodeAREA.selectionEnd = this.nodeAREA.value.length;
      this.nodeAREA.focus();
    }
  }

  addEventsListeners() {
    document.addEventListener('keydown', (event) => this.eventKeyDown(event));
    document.addEventListener('keyup', (event) => this.eventKeyUp(event));
    document.addEventListener('mousedown', (event) => this.eventMouseDown(event));
  }

  // BOARD EVENTS
  // BOARD KEYS DOWN
  eventKeyDown(event) {
    // initial statement
    if (keycode.includes(event.code)) {
      event.preventDefault();
    } else {
      return;
    }
    // keys highlight
    if (keycode.includes(event.code)) {
      document.getElementById(`${event.code}`).classList.add('keyactive');
    } else {
      return;
    }
    // switch language
    if (((event.code === 'ShiftLeft') && event.altKey) || (event.shiftKey && (event.code === 'AltLeft'))) {
      if (this.key_lang === 'key_eng') {
        this.key_lang = 'key_rus';
      } else {
        this.key_lang = 'key_eng';
      }
      localStorage.setItem('jun#2774VkbLang', this.key_lang);
      this.createKeysLayout();
    }
    // events caps down
    if (event.code === 'CapsLock') {
      if (this.key_caps === false) {
        this.key_caps = true;
      } else {
        this.key_caps = false;
      }
      this.createKeysLayout();
    }
    // events shift down
    if ((!event.altKey || event.code !== 'AltLeft') && (event.code === 'ShiftLeft' || event.code === 'ShiftRight')) {
      if (this.key_shift === false) {
        this.key_shift = true;
      } else {
        this.key_shift = false;
      }
      this.createKeysLayout();
    }
    // special keys
    switch (event.code) {
      // can be used textContent
      case 'Backspace':
        this.editSelectedKeys('backsp');
        break;
      case 'Tab':
        this.nodeAREA.value += '    ';
        break;
      case 'Delete':
        this.editSelectedKeys('delete');
        break;
      case 'Enter':
        this.nodeAREA.value += '\n';
        break;
      case 'Space':
        this.nodeAREA.value += ' ';
        break;
      case 'CapsLock':
      case 'ShiftLeft':
      case 'ShiftRight':
      case 'ControlLeft':
      case 'MetaLeft':
      case 'AltLeft':
      case 'AltRight':
      case 'ControlRight':
        break;
      default:
        if (keycode.includes(event.code)) {
          this.printCurrentKey(event.code);
        } else {
          break;
        }
        break;
    }
  }

  // BOARD KEYS UP
  eventKeyUp(event) {
    // initial statement
    if (keycode.includes(event.code)) {
      event.preventDefault();
    } else {
      return;
    }
    // keys highlight
    if (keycode.includes(event.code)) {
      document.getElementById(`${event.code}`).classList.remove('keyactive');
    } else {
      return;
    }
    // events shift up
    if ((!event.altKey || event.code !== 'AltLeft') && (event.code === 'ShiftLeft' || event.code === 'ShiftRight')) {
      if (this.key_shift === false) {
        this.key_shift = true;
      } else {
        this.key_shift = false;
      }
      this.createKeysLayout();
    }
  }

  // MOUSE EVENTS
  // MOUSE KEYS DOWN
  eventMouseDown(event) {
    // initial statement
    if (keycode.includes(event.target.id)) {
      event.preventDefault();
    } else {
      return;
    }
    // mouse caps down
    if (event.target.id === 'CapsLock') {
      if (this.key_caps === false) {
        this.key_caps = true;
      } else {
        this.key_caps = false;
      }
      this.createKeysLayout();
    }
    // mouse shift down
    if ((event.target.id === 'ShiftLeft') || (event.target.id === 'ShiftRight')) {
      if (this.key_shift === false) {
        this.key_shift = true;
      } else {
        this.key_shift = false;
      }
      this.createKeysLayout();
    }
    // special keys
    switch (event.target.id) {
      // can be used textContent
      case 'Backspace':
        this.editSelectedKeys('backsp');
        break;
      case 'Tab':
        this.nodeAREA.value += '    ';
        break;
      case 'Delete':
        this.editSelectedKeys('delete');
        break;
      case 'Enter':
        this.nodeAREA.value += '\n';
        break;
      case 'Space':
        this.nodeAREA.value += ' ';
        break;
      case 'CapsLock':
      case 'ShiftLeft':
      case 'ShiftRight':
      case 'ControlLeft':
      case 'MetaLeft':
      case 'AltLeft':
      case 'AltRight':
      case 'ControlRight':
        break;
      default:
        if (keycode.includes(event.target.id) && (this.key_shift === true)) {
          this.printCurrentKey(event.target.id);
          this.key_shift = false;
          this.createKeysLayout();
        } else if (keycode.includes(event.target.id) && (this.key_shift === false)) {
          this.printCurrentKey(event.target.id);
        } else {
          break;
        }
        break;
    }
  }
}

